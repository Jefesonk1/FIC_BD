    private boolean checarExisteciaNaTabela(JTable jTable, String elemento, int posicao) {
        for (int i = 0; i < jTable.getRowCount(); i++) {
            if (jTable.getValueAt(i, posicao).toString().equals(elemento)) {
                return true;
            }
        }
        return false;
    }


    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        if (evt.getClickCount() == 2) {
            try {
                String quantidade = JOptionPane.showInputDialog("Digite a quantidade desejada");
                int selectedRowIndex = jTable2.getSelectedRow();
                if (Integer.parseInt(quantidade) <= 0 || checarExisteciaNaTabela(jTable3, jTable2.getValueAt(selectedRowIndex, 0).toString(), 1)) {
                    return;
                }
                DefaultTableModel curriculumSubjectsModel = (DefaultTableModel) jTable3.getModel();
                Vector v = new Vector();
                v.add(quantidade);
                for (int i = 0; i < jTable2.getColumnCount(); i++) {
                    v.add(jTable2.getValueAt(selectedRowIndex, i));
                }
                curriculumSubjectsModel.addRow(v);
                jTable3.setModel(curriculumSubjectsModel);
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jTable2MouseClicked

